#include "main.h"
/**
 * print_sign - main function
 *
 * @n: an ASCII character to compae with logic
 *
 * Return: 1 if number is positive, -1 if negative and 0 otherwise
 */
int print_sign(int n)
{
	if (n > 0)
		_putchar('+');
	return (1);

	if (n < 0)
		_putchar('-');
	return (-1);

		_putchar('0');
		return (0);
}
